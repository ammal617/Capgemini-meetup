/*
	Den här funktionen skriver ut information om innehållet i en container. 
	Skriv om variablerna så att den skriver ut.
	
	Cars: None
	Motorcycles: 4
	Weight: 5.5
	Shipped by boat: true
 */

public class Variables1 {

	public static void main(String[] args) {
		int cars = 4;
		String motorcycles = "Manymany";
		int weight = 5;
		boolean shippedByBoat = false;
		
		System.out.println("Cars: " + cars);
		System.out.println("Motorcycles: " + motorcycles);
		System.out.println("Weight: " + weight);
		System.out.println("Shipped by boat: " + shippedByBoat);
	}

}